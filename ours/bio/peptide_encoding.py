# ===================== 核心修复2：特征生成主函数 (彻底重构，输出标准三维张量) =====================
def feature_generator(file_path):
    # 第一步：读取所有序列，提取原始序列+获取最长序列长度
    fasta_list = open(file_path, 'r', encoding='utf-8').readlines()
    all_sequences = []  # 存储所有原始蛋白序列
    flag = 0
    while flag < len(fasta_list):
        if fasta_list[flag].startswith('>'):
            seq = fasta_list[flag+1].strip('\n').strip()
            all_sequences.append(seq)
        flag += 2
    max_seq_len = max([len(s) for s in all_sequences])  # 所有序列统一对齐到这个长度
    fixed_feat_dim = 20 + 5  # 固定25维特征，永不改变！
    
    # 第二步：逐个序列提取特征 + 补零对齐
    all_features = []
    flag = 0
    for seq in all_sequences:
        seq_len = len(seq)
        seq_feat = []
        # 对每个氨基酸提取25维特征
        for aa in seq:
            b62 = get_blosum62(aa)
            zsc = get_zscale(aa)
            aa_feat = b62 + zsc  # 单个氨基酸=25维，固定列数！
            seq_feat.append(aa_feat)
        
        # 补零对齐：短序列末尾补零，形状统一为 [max_seq_len, 25]
        if seq_len < max_seq_len:
            pad_zeros = np.zeros((max_seq_len - seq_len, fixed_feat_dim)).tolist()
            seq_feat += pad_zeros
        
        # 转换为数组，每个样本的形状都是 [max_seq_len, 25]
        seq_feat = np.array(seq_feat, dtype=np.float32)
        all_features.append(seq_feat)
    
    # 第三步：用np.stack拼接，输出标准三维张量 [样本数, max_seq_len, 25] ✅✅✅
    # np.stack 是解决多维数组拼接的终极方案，彻底解决不均匀维度问题！
    all_features = np.stack(all_features, axis=0)
    return all_features